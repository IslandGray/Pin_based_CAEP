package com.wcp.DAO;

public class X3 {
	private String account;
	private int excute;
	private int report;
	private int another;
	private int score;
	private String addon;
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public int getExcute() {
		return excute;
	}
	public void setExcute(int excute) {
		this.excute = excute;
	}
	public int getReport() {
		return report;
	}
	public void setReport(int report) {
		this.report = report;
	}
	public int getAnother() {
		return another;
	}
	public void setAnother(int another) {
		this.another = another;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public String getAddon() {
		return addon;
	}
	public void setAddon(String addon) {
		this.addon = addon;
	}
	@Override
	public String toString() {
		return "X3 [account=" + account + ", excute=" + excute + ", report=" + report + ", another=" + another
				+ ", score=" + score + ", addon=" + addon + "]";
	}
	
	
}
